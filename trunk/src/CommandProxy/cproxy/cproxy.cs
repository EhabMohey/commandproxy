public class cproxy
{
    /// <summary>
    ///     main entry point for command line application
    /// </summary>
    public static void Main()
    {
        //todo: set these properties to be passed in via the command line
        
        //hard code port right now
        int port = 10000;

        //dont require auth token
        bool requireAuthToken = false;

        //create a new instance of the command proxy
        CommandProxy.CommandProxy proxy = new CommandProxy.CommandProxy(requireAuthToken, port);

        //start the run loop, passing in the port to use, and whether an auth
        //token should be used
        proxy.Run();
    }
